public class Addition {


    public Addition(){

    }

    public int add(int a, int b, int c) {
        return a + b + c;
    }

    public double add(double a, double b) {
        return a + b ;
    }

    public String add(String a, String b, String c) {
        return a + b + c;
    }

    public float add(float a, float b) {
        return a + b;
    }


}
